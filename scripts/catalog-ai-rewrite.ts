interface CatalogEntry {
    targetSmmplanTier: string;
    platform: string;
    action: string;
    tier: string;
    name: string;
    description: string;
    requirements: string;
}

function parseTargetTier(raw: string): { platform: string; action: string; tier: string } {
    // "[Вконтакте] Вконтакте - Просмотры на пост (Эконом)"
    const match = raw.match(/\[(.*?)\]\s*(.*?)\s*-\s*(.*?)\s*\((.*?)\)/);
    if (match) {
        return { platform: match[1], action: match[3].trim(), tier: match[4].trim() };
    }
    // Fallback для нестандартных форматов
    return { platform: 'Неизвестная', action: raw, tier: 'Стандарт' };
}

function findActionTemplate(action: string): { base: string; requirements: string } {
    const actionLower = action.toLowerCase();
    
    // Точное совпадение ключа
    for (const [key, val] of Object.entries(ACTION_TEMPLATES)) {
        if (actionLower.includes(key)) return val;
    }
    
    // Частичное: отделяем первое слово-действие
    const firstWord = actionLower.split(' ')[0];
    for (const [key, val] of Object.entries(ACTION_TEMPLATES)) {
        if (key.startsWith(firstWord)) return val;
    }

    return {
        base: 'Комплексное продвижение для повышения видимости и роста показателей.',
        requirements: 'Контент должен быть публичным.'
    };
}

function generatePremiumDescription(platform: string, action: string, tier: string): { description: string; requirements: string } {
    const template = findActionTemplate(action);
    const tierLabel = TIER_LABELS[tier.toLowerCase()] || `Категория: ${tier}`;

    const description = `${template.base}\n\n• **Класс:** ${tier}\n• **Уровень:** ${tierLabel}\n• **Платформа:** ${platform}`;
    return { description, requirements: template.requirements };
}

function run() {
    console.log('📖 Чтение эталонного каталога...');
    
    const lines = fs.readFileSync(CSV_FILE, 'utf8').split('\n').filter(l => l.trim().length > 0);
    lines.shift(); // skip header

    const catalog: CatalogEntry[] = [];

    for (const line of lines) {
        const cols = line.split(';').map(c => c.replace(/^"|"$/g, ''));
        const raw = cols[0];
        const { platform, action, tier } = parseTargetTier(raw);
        const { description, requirements } = generatePremiumDescription(platform, action, tier);

        catalog.push({
            targetSmmplanTier: raw,
            platform,
            action,
            tier,
            name: `${action} (${tier})`,
            description,
            requirements
        });
    }

    // Сохраняем
    fs.writeFileSync('final_catalog_premium.json', JSON.stringify(catalog, null, 2));
    
    // Статистика
    const platforms = [...new Set(catalog.map(c => c.platform))];
    const actions = [...new Set(catalog.map(c => c.action))];

    console.log(`\n✅ ГОТОВО! Сгенерировано ${catalog.length} премиум-описаний.`);
    console.log(`📊 Платформы (${platforms.length}): ${platforms.join(', ')}`);
    console.log(`📊 Уникальных типов действий: ${actions.length}`);
    console.log(`📁 Файл: final_catalog_premium.json`);
}

run();
